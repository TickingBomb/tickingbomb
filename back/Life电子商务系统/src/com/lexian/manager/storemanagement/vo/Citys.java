package com.chinasofti.lexian.manager.storemanagement.vo;

public class Citys {
	private int id;
	private String cityid;
	private String city;
	private String city_py;
	private String city_pym;
	private int clazz;
	private int parent_id;
	private String postcode;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCityid() {
		return cityid;
	}
	public void setCityid(String cityid) {
		this.cityid = cityid;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCity_py() {
		return city_py;
	}
	public void setCity_py(String city_py) {
		this.city_py = city_py;
	}
	public String getCity_pym() {
		return city_pym;
	}
	public void setCity_pym(String city_pym) {
		this.city_pym = city_pym;
	}
	public int getClazz() {
		return clazz;
	}
	public void setClazz(int clazz) {
		this.clazz = clazz;
	}
	public int getParent_id() {
		return parent_id;
	}
	public void setParent_id(int parent_id) {
		this.parent_id = parent_id;
	}
	public String getPostcode() {
		return postcode;
	}
	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}

}
